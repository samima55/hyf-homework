/** array that has an extra key called tag.
 *  The tag is based on the rating: 
 * Good (>= 7), Average (>= 4 and < 7), Bad (< 4) */
const moviesWitTag = movies.map(movie => {
    if (movie.rating >= 7) {
        movie.tag = "Good";
    }

    else if (movie.rating >= 4 && movie.rating < 7) {
        movie.tag = "Average";
    }
    else if (movie.rating < 4) {
        movie.tag = "Bad"
    }
    return movie;
})
console.log('movie with tag ', moviesWitTag);

