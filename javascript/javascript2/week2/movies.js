/**Count the total number of movies containing
 *  any of following keywords:
 *  Surfer, Alien or Benjamin.
 *  So if there were 3 movies that contained Surfer,
 *  1 with Alien and 2 with Benjamin, you would return 6.
 *  Can you make sure the search is case insensitive? */


const keyWords = ["surfer", "alien", "bejamin"];

const keyWOrdsMovies = movies.filter(movie => {
    const title = movie.title.toLocaleLowerCase();
    return keyWords.some(keyword => title.includes(keyword.toLocaleLowerCase()))
}).length;

console.log(' count of movie with special key', keyWOrdsMovies);


/**Create an array of movies where a word 
 * in the title is duplicated.  */

const duplicatedMovies = movies.filter(movie => {
    const words = movie.title.toLowerCase().split(" ");
    const duplicateWords = words.filter((word, index) => {
        return words.indexOf(word) !== index;
    });
    return duplicateWords.length > 0;
});
console.log('duplicated title', duplicatedMovies);

